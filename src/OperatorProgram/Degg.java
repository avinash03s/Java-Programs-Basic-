package OperatorProgram;

public class Degg {
    public static void main(String[] args) {
        int a=358;
        System.out.println(--a);//357
        System.out.println(a);//357

        int b=495;
        System.out.println(b--);//495
        System.out.println(b);//494

        boolean t=true;
        System.out.println(!t);
        boolean p=false;
        System.out.println(!p);
    }
}
