package FlowControlStatement;

public class Months {
    public static void main(String[] args) {
        int month=2;

        switch (month){
            case 1:
                System.out.println("jan");
                break;
            case 2:
                System.out.println("feb");
                break;
            case 3:
                System.out.println("mar");
                break;
        }
       // System.out.println(month);
    }
}
