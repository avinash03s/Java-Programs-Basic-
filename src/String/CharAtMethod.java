package String;

//charAt() method used character locating at specified index

public class CharAtMethod {
    public static void main(String[] args) {
        String s1="Avinash";
                 //0123456
        System.out.println(s1.charAt(6));
        String s2= "avinashsurwase";
                  //0123456789....
        System.out.println(s2.charAt(8));
    }
}
