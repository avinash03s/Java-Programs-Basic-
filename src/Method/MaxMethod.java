package Method;

public class MaxMethod {
    public static void main(String[] args) {
        int a=400,b=500;
        int max=Math.max(a,b);
        int min=Math.min(a,b);
        System.out.println("A Max Value between a&b :"+max);
        System.out.println("A Min value between a&b :"+min);
    }
}
